import { NestFactory } from '@nestjs/core';
import { ValidationPipe } from '@nestjs/common';
import { DocumentBuilder, SwaggerModule } from '@nestjs/swagger';
import { ConfigService } from '@nestjs/config';
import * as compression from 'compression';
import * as express from 'express';
import helmet from 'helmet';
import { AppModule } from './app.module';
import { AllExceptionsFilter } from './common/filters/http-exception.filter';
import { ResponseInterceptor } from './common/interceptors/response.interceptor';
// eslint-disable-next-line @typescript-eslint/no-unused-vars
import { LoggerMiddleware } from './common/middlewares/logger.middleware';
import { LoggerService } from './shared/logger/logger.service';

async function bootstrap() {
  const app = await NestFactory.create(AppModule, {
    bufferLogs: true,
  });

  const configService = app.get(ConfigService);
  const loggerService = app.get(LoggerService);

  // ‰ΩøÁî®Ëá™ÂÆö‰πâÊó•ÂøóÊúçÂä°
  app.useLogger(loggerService);

  // ÂÖ®Â±ÄÂâçÁºÄÔºåÊéíÈô§Ê†πË∑ØÂæÑÂíåAPI‰ø°ÊÅØË∑ØÂæÑ
  app.setGlobalPrefix('api/v1', {
    exclude: [
      '', // Ê†πË∑ØÂæÑ /
      'api', // API‰ø°ÊÅØË∑ØÂæÑ /api
      'api/docs', // SwaggerÊñáÊ°£Ë∑ØÂæÑ
    ],
  });

  // ÂÆâÂÖ®‰∏≠Èó¥‰ª∂
  app.use(
    helmet({
      contentSecurityPolicy: {
        directives: {
          defaultSrc: ["'self'"],
          styleSrc: ["'self'", "'unsafe-inline'"],
          scriptSrc: ["'self'"],
          imgSrc: ["'self'", 'data:', 'https:'],
        },
      },
      crossOriginEmbedderPolicy: false,
    }),
  );

  // CORSÈÖçÁΩÆ
  app.enableCors({
    origin: configService.get('ALLOWED_ORIGINS')?.split(',') || [
      'http://localhost:3000',
    ],
    credentials: true,
    methods: ['GET', 'POST', 'PUT', 'DELETE', 'PATCH', 'OPTIONS'],
    allowedHeaders: ['Content-Type', 'Authorization', 'X-Requested-With'],
  });

  // ÂìçÂ∫îÂéãÁº©
  app.use(compression());

  // ËØ∑Ê±ÇÂ§ßÂ∞èÈôêÂà∂
  app.use(express.json({ limit: '10mb' }));
  app.use(express.urlencoded({ extended: true, limit: '10mb' }));

  // ÂÖ®Â±ÄÁÆ°ÈÅì
  app.useGlobalPipes(
    new ValidationPipe({
      whitelist: true,
      forbidNonWhitelisted: true,
      transform: true,
      disableErrorMessages: configService.get('NODE_ENV') === 'production',
    }),
  );

  // ÂÖ®Â±ÄËøáÊª§Âô®
  app.useGlobalFilters(new AllExceptionsFilter());

  // ÂÖ®Â±ÄÊã¶Êà™Âô®
  app.useGlobalInterceptors(new ResponseInterceptor());

  // SwaggerÊñáÊ°£ÈÖçÁΩÆ
  if (configService.get('NODE_ENV') !== 'production') {
    const config = new DocumentBuilder()
      .setTitle('Enterprise NestJS API')
      .setDescription('‰ºÅ‰∏öÁ∫ßNestJSÂêéÁ´ØAPIÊñáÊ°£')
      .setVersion('1.0')
      .addBearerAuth(
        {
          type: 'http',
          scheme: 'bearer',
          bearerFormat: 'JWT',
          name: 'JWT',
          description: 'Enter JWT token',
          in: 'header',
        },
        'JWT-auth',
      )
      .addTag('Auth', 'ËÆ§ËØÅÁõ∏ÂÖ≥Êé•Âè£')
      .addTag('Users', 'Áî®Êà∑ÁÆ°ÁêÜ')
      .addTag('Admin', 'ÁÆ°ÁêÜÂêéÂè∞')
      .addTag('Public', 'ÂÖ¨ÂºÄÊé•Âè£')
      .addTag('Health', 'ÂÅ•Â∫∑Ê£ÄÊü•')
      .build();

    const document = SwaggerModule.createDocument(app, config);
    SwaggerModule.setup('api/docs', app, document, {
      swaggerOptions: {
        persistAuthorization: true,
      },
    });
  }

  const port =
    configService.get('app.port') || configService.get('PORT') || 8000;
  const host =
    configService.get('app.host') || configService.get('HOST') || 'localhost';

  await app.listen(port, host);

  loggerService.log(`üöÄ Application is running on: http://${host}:${port}`);
  loggerService.log(`üìö API Documentation: http://${host}:${port}/api/docs`);
}

bootstrap().catch(error => {
  console.error('‚ùå Error starting the application:', error);
  process.exit(1);
});
